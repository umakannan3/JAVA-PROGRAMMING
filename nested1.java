package Start.src;

public class nested1
{
    public static void main(String args[])
    {

            for(int i=1;i<=5;i++)//1<=5 2<=5
            {
                for(int j=1;j<=i;j++)
                {
                    System.out.print("*");
                }
                System.out.println("");
            }
    }
}

